# planner.py
# Planner 的职责：只负责“把任务拆成步骤”，不直接执行任何工具
# 这是一个非常简化的规划器示例


def plan(task: str) -> list[str]:
    """
    将一个用户任务拆解为可执行步骤。

    参数:
        task (str): 用户提出的自然语言任务

    返回:
        list[str]: 按顺序的步骤列表，供 Agent 按序执行
    """
    # 当前实现返回一个固定的三步计划；实际场景中可根据 task 做更细粒度的拆分
    return [
        "理解问题背景",
        "分析核心概念",
        "给出简要总结"
    ]
